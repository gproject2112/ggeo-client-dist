    t: function(key, fallback) {
        var dict = this.translations[this.lang] || this.translations.en;
        if (dict[key] !== undefined) return dict[key];
        if (this.translations.en[key] !== undefined) return this.translations.en[key];
        return fallback != null ? fallback : key;
    },

    apply: function() {
        var self = this;
        document.querySelectorAll("[data-i18n]").forEach(function(el) {
            var key = el.getAttribute("data-i18n");
            el.textContent = self.t(key);
        });
        document.querySelectorAll("[data-i18n-placeholder]").forEach(function(el) {
            var key = el.getAttribute("data-i18n-placeholder");
            el.setAttribute("placeholder", self.t(key));
        });
        document.querySelectorAll("[data-i18n-title]").forEach(function(el) {
            var key = el.getAttribute("data-i18n-title");
            el.setAttribute("title", self.t(key));
        });
        document.querySelectorAll(".lang-toggle-btn").forEach(function(btn) {
            btn.classList.toggle("active", btn.dataset.lang === self.lang);
        });
    },

    toggle: function(lang) {
        if (!this.translations[lang]) return;
        this.lang = lang;
        try { localStorage.setItem("ggeo_lang", lang); } catch (e) {}
        this.apply();
    },

    init: function() {
        var saved = null;
        try { saved = localStorage.getItem("ggeo_lang"); } catch (e) {}
        if (saved && this.translations[saved]) {
            this.lang = saved;
        } else {
            this.lang = "id";
        }
        this.apply();
    },
